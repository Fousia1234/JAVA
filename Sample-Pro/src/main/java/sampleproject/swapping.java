package sampleproject;

public class swapping {
	public static void main(String args[]) {
		int x=200;
		int y=400;
		System.out.println("Before swap is");
		System.out.println("x = "+x);
		System.out.println("y = "+y);
		 int temp=x;
		x=y;
		y=temp;
		System.out.println("After swap is");
		System.out.println("x = "+x);
		System.out.println("y = "+y);
		
	}

}
